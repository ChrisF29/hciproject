/**
 * Get words by category
 */
function getWordsByCategory($category) {
    global $wordDatabase;
    return array_filter($wordDatabase, function($word) use ($category) {
        return strtolower($word['category']) === strtolower($category);
    });
}

/**
 * Get all categories
 */
function getAllCategories() {
    global $wordDatabase;
    $categories = array_unique(array_column($wordDatabase, 'category'));
    sort($categories);
    return $categories;
}

/**
 * Get words by length
 */
function getWordsByLength($minLength, $maxLength) {
    global $wordDatabase;
    return array_filter($wordDatabase, function($word) use ($minLength, $maxLength) {
        $len = strlen($word['word']);
        return $len >= $minLength && $len <= $maxLength;
    });
}
